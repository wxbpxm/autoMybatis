package fuku.automybatis.bean;

public class BusinessPojo {
    /**
     *说明：主键
     */
    private String id;

    /**
     *说明：模型id
     */
    private String businessId;

    /**
     *说明：pojo的id
     */
    private String pojoId;

    /**
     *说明：mongodb及pojo的字段名
     */
    private String columnName;

    /**
     *说明：新增页面 000:String 100:BigDecimal 200:Long 300:Integer 400:List 500:Pojo等
     */
    private String javaType;

    /**
     *说明：排序字段
     */
    private Integer sort;

    /**
     *说明：mongodb及pojo的字段说明
     */
    private String columnRemark;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBusinessId() {
        return businessId;
    }

    public void setBusinessId(String businessId) {
        this.businessId = businessId;
    }

    public String getPojoId() {
        return pojoId;
    }

    public void setPojoId(String pojoId) {
        this.pojoId = pojoId;
    }

    public String getColumnName() {
        return columnName;
    }

    public void setColumnName(String columnName) {
        this.columnName = columnName;
    }

    public String getJavaType() {
        return javaType;
    }

    public void setJavaType(String javaType) {
        this.javaType = javaType;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public String getColumnRemark() {
        return columnRemark;
    }

    public void setColumnRemark(String columnRemark) {
        this.columnRemark = columnRemark;
    }
}